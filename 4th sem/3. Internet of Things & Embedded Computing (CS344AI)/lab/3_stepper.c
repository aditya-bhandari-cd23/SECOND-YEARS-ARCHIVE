#include<lpc214x.h>
#define LED_OFF IO0SET = 1<<31
#define LED_ON  IO0CLR = 1<<31

void delayMs(int x)
{
    for (int i = 0; i < x; i++)
        for (int j = 0; j < 10000; j++);
}

int main()
{
    IO0DIR |= 1 << 31 | 0x00FF0000;
    int clks = 100, aclks = 100, i;
    for (i = 0; i < clks / 4; i++) {
        IO0CLR = 0x000F0000;////////////////////////////////////////
        IO0SET = 0x00010000;
        delayMs(10);
        IO0CLR = 0x000F0000;////////////////////////////////////////
        IO0SET = 0x00020000;
        delayMs(10);
        IO0CLR = 0x000F0000;////////////////////////////////////////
        IO0SET = 0x00040000;
        delayMs(10);
        IO0CLR = 0x000F0000;////////////////////////////////////////
        IO0SET = 0x00080000;
        delayMs(10);
    }
    for (i = 0; i < clks / 4; i++) {
        IO0CLR = 0x000F0000;////////////////////////////////////////
        IO0SET = 0x00080000;
        delayMs(10);
        IO0CLR = 0x000F0000;////////////////////////////////////////
        IO0SET = 0x00040000;
        delayMs(10);
        IO0CLR = 0x000F0000;////////////////////////////////////////
        IO0SET = 0x00020000;
        delayMs(10);
        IO0CLR = 0x000F0000;////////////////////////////////////////
        IO0SET = 0x00010000;
        delayMs(10);
    }
    IO0CLR = 0x00FF0000; ////////////////////////////////////////
    while (1);
}
